	public List<Map<String, Object>> onWeb(int start, int end) throws Exception {

        setChscid();
        setSchoolNet();
        ArrayList <Map<String, Object>> resultList  = new ArrayList <Map<String, Object>>();
		int eng=0;
		for(int i=start; i<end; i++){

			HashMap <String, Object> entity = new HashMap <String, Object> ();

			for(int j=0; j<URL.length; j++){
				//URL url = new URL(URL[j] );
				URL url = new URL(URL[j] + i );
				//System.out.println(url.toString());
				//String result = getURLContent(url,"gbk") ;
                String result = getURLContent(url,"UTF-8") ;

                //System.out.println(result);
				//result = result.replaceAll("<br>", "");
				result = result.replaceAll("<p align='left'>-</p>", "");


				HashMap<String,String> map = searchKeyList.get(j);
				for (String key : map.keySet()) {
					//System.out.println(key + " " + map.get(key));
					eng=0;
					Pattern p = Pattern.compile(map.get(key));
				    Matcher matcher = p.matcher(result);
				    boolean matchFound = matcher.find();
				    if(matchFound) {
				        if("financetype".equalsIgnoreCase(key) && matcher.group(2).contains("<br>") ){
				    		String tmp[] = matcher.group(2).split("<br>");
				    		if(tmp.length > 1){
				    			entity.put((eng==0) ? key : key+ "eng", tmp[0] );
				    			entity.put((eng==0) ? "session" : key+ "eng", tmp[1] );
				    		}
				    	}else if("financetype".equalsIgnoreCase(key) && matcher.group(2).contains("<br>") ){
				    		String tmp[] = matcher.group(2).split("<br>");
				    		if(tmp.length > 1){
				    			entity.put((eng==0) ? key : key+ "eng", tmp[0] );
				    			entity.put((eng==0) ? "session" : key+ "eng", tmp[1] );
				    		}
				    	}else if("schoolfee".equalsIgnoreCase(key) || "schoolfeevoucher".equalsIgnoreCase(key) || "tuitionfee".equalsIgnoreCase(key)||  "area".equalsIgnoreCase(key) ||
							 "teacherexperience".equalsIgnoreCase(key) ||  "teacherbachelor".equalsIgnoreCase(key)  ){
							  	entity.put((eng==0) ? key : key+ "eng", formatNumber(matcher.group(2)) );
				    	}else if("poaschoolnet".equalsIgnoreCase(key)){
				    		entity.put((eng==0) ? key : key+ "eng", removeString(matcher.group(2),"&amp;") );
						}else if("teacherpupilam".equalsIgnoreCase(key) || "teacherpupilpm".equalsIgnoreCase(key)){
				    		entity.put((eng==0) ? key : key+ "eng", matcher.group(2) != null && matcher.group(2).trim().length() > 1 ? matcher.group(2) : "\u6C92\u6709\u8CC7\u6599" );
                        }else if("session".equalsIgnoreCase(key)){
                                if("0".equals(matcher.group(2).trim())){
                                    entity.put(key,"半日制");
                                }else{
                                    entity.put(key,"全日制");
                                }
                        }else if("qareport".equalsIgnoreCase(key) || "qareporturl".equalsIgnoreCase(key)){
                            entity.put("qareport","質素評核報告");
                            entity.put("qareporturl","http://www.edb.gov.hk/tc/edu-system/preprimary-kindergarten/quality-assurance-framework/qr/qr-report/index.html");
                        }else{
				    		entity.put((eng==0) ? key : key+ "eng", matcher.group(2).trim());
				    	}

				        //System.out.println(key + " " + matcher.group(2).trim());
                        //System.out.println(key + " " + entity.get(key));

                        matchFound = matcher.find(matcher.end());
				        eng++;
				    }
				}
			}
            if( "\u6709".equalsIgnoreCase((String) entity.get("voucher"))){
				entity.put("schoolfee", entity.get("schoolfeevoucher"));
			}
			if( entity.get("noofclassroom") != null){
				try{
					Integer.parseInt((String) entity.get("noofclassroom"));
				}catch (Exception ex){
					entity.put("noofclassroom",0);
				}
				entity.put("area",entity.get("noofclassroom"));
			}
			if( entity.get("name") != null && ((String) entity.get("name")).trim().isEmpty()  ){
				entity.put("name", entity.get("nameeng"));
			}
            if( entity.get("name") == null  ){
                entity.put("name", entity.get("nameeng"));
            }
			try{
				Integer.parseInt((String) entity.get("schoolfee"));
			}catch (Exception ex){
				entity.put("schoolfee",0);
			}
            try{
                entity.put("teacherexperience" ,  Integer.parseInt((String) entity.get("totalstaff"))  * Integer.parseInt((String) entity.get("teacherexperience")) / 100 )  ;
            }catch (Exception ex){
                entity.put("teacherexperience",0);
                entity.put("totalstaff",0);
            }

			//System.out.println("schoolfee " + entity.get("schoolfee"));
			entity.remove("schoolfeevoucher");

			entity.put("categoryid", "0");
            entity.put("poaschoolnet",getSchoolNet("" + entity.get("poaschoolnet")));
			String chscid = getChscId("" + entity.get("nameeng") + "-"  + entity.get("poaschoolnet"));
            if(entity.get("nameeng") != null ){
                if(chscid == null || chscid.trim().length() < 1){
                    chscid = getChscId("" + entity.get("name") + "-" + entity.get("poaschoolnet"));
                    if(chscid == "11111" ){
                    System.out.println("name eng not found"
                        + entity.get("nameeng")
                        + " -- chi name:"
                        + entity.get("name")
                        + " chscid: "
                        + chscid);
                    }
                }
            }
            //System.out.println("chscid " + chscid + " added");

            entity.put("chscid", (chscid != null && chscid.trim().length() > 0) ? chscid :  "" + i  );
            entity.put("chscidnew", "" + i);
			if(!entity.isEmpty() && entity.containsKey("name")
                    && entity.get("name") != null && entity.get("name").toString().trim().length() > 0
                    ){
				//System.out.println("chscid " + entity.get("chscid") + " added");
				resultList.add(entity);
			}
            if("683".equalsIgnoreCase(""+entity.get("chscid"))) {
                for (String key : entity.keySet()) {
                    System.out.println("Spider -- " + key + ":" + entity.get(key));
                }
            }

		}
		return resultList;


	}

